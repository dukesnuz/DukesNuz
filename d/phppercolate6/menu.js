$(document).ready(function() {
 $('#chapter1').hide();
$('#chaptersselect1').toggle(
	function(){
	    $('#chapter1').fadeIn(1500);
		$('#excercises').show().fadeIn(4000);
		$('#excercises').css('background','blue');
		$('#excercises').css('font-size','1.875em');
		$('#excercises').css('color','#FFF');
		$('#excercises').html('Select an excercise above');
		$('#excercises').css('border' , '3px solid #FF00FF');
		$('#welcome').hide().fadeOut(4000);
		$('#chaptersselect1').html('CLOSE');
		$('#chaptersselect1').css('color','#FF6347');
		$('#chaptersselect1').css('background-color','#000000');
	},
	function(){
	    $('#chapter1').fadeOut(3000);
		$('#chaptersselect1').html('Chapter 1');
		$('#chaptersselect1').css('color','#2F4F4F');
	    $('#welcome').show().fadeIn(4000);
		$('#excercises').hide().fadeOut(4000);
		$('#chaptersselect1').css('background-color','#FFFFFF');
	}
	); //end toggle
}); // end ready


