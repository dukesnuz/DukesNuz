// JavaScript Document
$(document).ready(function() {
 $('#chapter1').hide();
$('#chaptersselect1').toggle(
	function(){
	    $('#chapter1').fadeIn(1500);
		$('#chaptersselect1').html('CLOSE');
		$('#chaptersselect1').css('color','#FF6347');
	},
	function(){
	    $('#chapter1').fadeOut(3000);
		$('#chaptersselect1').html('Chapter 1');
		$('#chaptersselect1').css('color','#2F4F4F');
	    //
	   //$('#excercises').hide().fadeOut(4000);
		
	}
	); //end toggle
}); // end ready


